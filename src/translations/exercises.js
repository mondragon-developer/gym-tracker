/**
 * Translate an exercise name based on current language
 * Falls back to original name if translation not found
 */
export const translateExercise = (exerciseName, language = 'en') => {
    if (language === 'es' && exerciseTranslations[exerciseName]) {
        return exerciseTranslations[exerciseName];
    }
    return exerciseName;
};

/**
 * Translate muscle group combinations (like "Chest & Shoulders & Triceps")
 * Handles both individual muscle groups and combinations with "&"
 */
export const translateMuscleGroup = (muscleGroupName, language = 'en') => {
    if (language === 'en') {
        return muscleGroupName;
    }
    
    // Split by " & " and translate each part
    const parts = muscleGroupName.split(' & ');
    const translatedParts = parts.map(part => {
        const trimmedPart = part.trim();
        return exerciseTranslations[trimmedPart] || trimmedPart;
    });
    
    return translatedParts.join(' y ');
};